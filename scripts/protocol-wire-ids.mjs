#!/usr/bin/env node
// Reports the wire-tag index (SCALE enum discriminant) of every variant in the
// host <-> product protocol enum.
//
// The enum is built in packages/host-api/src/protocol/messageCodec.ts by walking
// `hostApiProtocol` (impl.ts) in declaration order and flattening each method into
// variants. scale-ts `Enum` assigns indices by insertion order, starting at 0.
// This script reproduces that flattening from source, so it needs no build step
// and no node_modules.
//
// Flattening rule (mirrors messageCodec.ts createPayload):
//   request   -> <method>_request, <method>_response
//   subscribe -> <method>_start, <method>_stop, <method>_interrupt, <method>_receive
//
// The discriminant is a single byte, so the enum is capped at 256 variants
// (indices 0..=255). See docs note generated alongside this report.
//
// Usage:
//   node scripts/protocol-wire-ids.mjs            # print report to stdout
//   node scripts/protocol-wire-ids.mjs -o FILE    # also write report to FILE
//   node scripts/protocol-wire-ids.mjs --json     # emit JSON instead of markdown

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join, relative } from 'node:path';

const MAX_VARIANTS = 256; // single-byte discriminant

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(here, '..');
const implPath = join(repoRoot, 'packages/host-api/src/protocol/impl.ts');

const VARIANTS_BY_KIND = {
  versionedRequest: ['request', 'response'],
  versionedSubscription: ['start', 'stop', 'interrupt', 'receive'],
};

/** Parse impl.ts and return the ordered list of { method, kind } entries. */
function parseProtocol(source) {
  // Matches lines like:  remote_chain_head_follow_subscribe: versionedSubscription({
  const re = /^\s*([a-z][a-z0-9_]*)\s*:\s*(versionedRequest|versionedSubscription)\s*\(/gm;
  const methods = [];
  let m;
  while ((m = re.exec(source)) !== null) {
    methods.push({ method: m[1], kind: m[2] });
  }
  if (methods.length === 0) {
    throw new Error(`No protocol methods found in ${implPath} — has the format changed?`);
  }
  return methods;
}

/** Expand methods into the flat, ordered list of enum variant tags. */
function buildVariants(methods) {
  const variants = [];
  for (const { method, kind } of methods) {
    const suffixes = VARIANTS_BY_KIND[kind];
    for (const suffix of suffixes) {
      variants.push({ tag: `${method}_${suffix}`, method, kind });
    }
  }
  return variants.map((v, index) => ({ index, ...v }));
}

function renderMarkdown(variants, methods) {
  const total = variants.length;
  const remaining = MAX_VARIANTS - total;
  const requests = methods.filter((m) => m.kind === 'versionedRequest').length;
  const subscriptions = methods.filter((m) => m.kind === 'versionedSubscription').length;
  const now = new Date().toISOString().slice(0, 10);

  const lines = [];
  lines.push('# Protocol wire-tag IDs');
  lines.push('');
  lines.push('> Generated by `scripts/protocol-wire-ids.mjs`. Do not edit by hand — re-run the script.');
  lines.push('');
  lines.push(`- Date: ${now}`);
  lines.push(`- Source: \`${relative(repoRoot, implPath)}\` (variant order) + \`packages/host-api/src/protocol/messageCodec.ts\` (flattening)`);
  lines.push(`- Methods: ${methods.length} (${requests} request, ${subscriptions} subscribe)`);
  lines.push(`- Variants (wire tags): **${total}** of max **${MAX_VARIANTS}**`);
  lines.push(`- Remaining capacity: **${remaining}** variants before the single-byte discriminant overflows`);
  lines.push('');
  lines.push('The discriminant is one byte; index `255` is the last representable value.');
  lines.push('');
  lines.push('| Index (dec) | Index (hex) | Wire tag | Method kind |');
  lines.push('| ----------: | ----------- | -------- | ----------- |');
  for (const v of variants) {
    const hex = '0x' + v.index.toString(16).toUpperCase().padStart(2, '0');
    const kind = v.kind === 'versionedRequest' ? 'request' : 'subscribe';
    lines.push(`| ${v.index} | ${hex} | \`${v.tag}\` | ${kind} |`);
  }
  lines.push('');
  return lines.join('\n');
}

function main() {
  const args = process.argv.slice(2);
  const asJson = args.includes('--json');
  const outIdx = args.findIndex((a) => a === '-o' || a === '--out');
  const outFile = outIdx !== -1 ? args[outIdx + 1] : undefined;

  const source = readFileSync(implPath, 'utf8');
  const methods = parseProtocol(source);
  const variants = buildVariants(methods);

  if (variants.length > MAX_VARIANTS) {
    console.error(
      `WARNING: ${variants.length} variants exceeds the ${MAX_VARIANTS}-variant single-byte limit.`,
    );
  }

  const output = asJson
    ? JSON.stringify(
        {
          generatedAt: new Date().toISOString(),
          maxVariants: MAX_VARIANTS,
          total: variants.length,
          remaining: MAX_VARIANTS - variants.length,
          variants,
        },
        null,
        2,
      )
    : renderMarkdown(variants, methods);

  process.stdout.write(output + '\n');
  if (outFile) {
    const target = outFile.startsWith('/') ? outFile : join(process.cwd(), outFile);
    writeFileSync(target, output + '\n');
    console.error(`Wrote report to ${target}`);
  }
}

main();
